#include "3-calc.h"

/**
* op_add - returns the sum of a and b
* @a: first number
* @b: second number
* Return: result.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - returns  a - b
* @a: first number
* @b: second number
* Return: result.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - returns  a * b
* @a: first number
* @b: second number
* Return: result.
*/
int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - returns  a - b
* @a: first number
* @b: second number
* Return: result.
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - returns  a % b
* @a: first number
* @b: second number
* Return: result.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
